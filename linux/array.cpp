#include <stdlib.h> // EXIT_SUCCESS

#include "../src/PipeGIF.h"
#include "ansiTerminal.h"

const unsigned char inlineImage[] = {
    0x47, 0x49, 0x46, 0x38, 0x39, 0x61, // GIF89a
    0x0a, 0x00, 0x0a, 0x00, 0x91, 0x00, 0x00, // 10x10, color count 4=2^(1+(0x91 & 0x07)
        0xff, 0xff, 0xff, // White Color
        0xff, 0x00, 0x00, // Red
        0x00, 0x00, 0xff, // Blue
        0x00, 0x00, 0x00, // not used, filler
    0x21, 0xf9, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, // Graphics Control Extension
    0x21, 0xfe, 0x48, 0x65, 0x6c, 0x6c, 0x6f, 0x2c, 0x20, 0x77, 0x6f, 0x72, 0x6c, 0x64, 0x21, 0x00, // Comment: Hello, world!
    0x2c, 0x00, 0x00, 0x00, 0x00, 0x0a, 0x00, 0x0a, 0x00, 0x00, // Image Descriptor 10x10
    0x02, // LZW code size = 2
    0x16, // blockSize = 22 bytes in this block
        0x8c, 0x2d, 0x99, 0x87, 0x2a, 0x1c, 0xdc, 0x33,
        0xa0, 0x02, 0x75, 0xec, 0x95, 0xfa, 0xa8, 0xde,
        0x60, 0x8c, 0x04, 0x91, 0x4c, 0x01,
    0x00, // blockSize of next block, 0 == end-of-LZW
    0x3b, // GIF file terminator
};

int main(void) {
    long unsigned int i;

    ansiTerminal ansiTerm(80, 25); // Text mode demo display
    PipeGIF gif( &ansiTerm ); // Decode GIF and send it to display

    for (i=0; i<sizeof(inlineImage); i++)
       gif.decode( inlineImage[i] );

    return EXIT_SUCCESS;
}
